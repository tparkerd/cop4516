import java.util.Scanner;


public class containers {
  public final static boolean DEBUG = true;

  public static void main(String[] args) {

    Scanner stdin = new Scanner(System.in);

    // Get each case and process it
    String tmpCase;
    while (stdin.hasNextLine()) {
      // Get the next case
      tmpCase = stdin.nextLine();
      // If the end of the file was reached, stop processing cases
      if (tmpCase.equals("end")) break;
      if (DEBUG) System.out.println(tmpCase);

      





    }

  }



}
